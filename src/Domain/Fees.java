package Domain;

public class Fees {

	
	int yearFee;
	int funeral;
	
	
	
	
	
	public void setYearFee(int yearFee) {
		this.yearFee = yearFee;
	}
	public void setFuneral(int funeral) {
		this.funeral = funeral;
	}
	public int getYearFee() {
		return yearFee;
	}
	public int getFuneral() {
		return funeral;
	}
	
	
	
	
}
